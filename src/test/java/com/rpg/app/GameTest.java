import static org.junit.Assert.assertEquals;
import org.junit.*;

public class GameTest {
	
	@Test
	public void testCheckWhichEnemy() {
		Game game = new Game();
		Enemy enemies[] = new Enemy[2];
		enemies[0] = new Enemy(10, 10, 10, 10, "TestMonsterNr.1", false);
		enemies[1] = new Enemy(10, 10, 10, 10, "TestMonsterNr.2", false);
		
		assertEquals(enemies[0], game.checkWhichEnemy(enemies[0].getxPosition(), enemies[0].getyPosition(), enemies));
	}
	
	@Test
	public void testFightOrFindNothingIfThereWasNoEncounter() {
		/* Testing if there was an encounter would be to match assertTrue(true) 
		 * since the method fight would return true, therefore considering it trivial, 
		 * it was not made as a case */
		Game game = new Game();
		Player p = new Player("NameTest", "Male", 'w');
		Enemy enemies[] = new Enemy[2];
		enemies[0] = new Enemy(10, 10, 10, 10, "TestMonsterNr.1", false);
		enemies[1] = new Enemy(10, 10, 10, 10, "TestMonsterNr.2", false);
		
		assertEquals(false, game.fightOrFindNothing(' ', p, enemies, enemies[0].getxPosition(), enemies[0].getyPosition()));
	}
}
